    private void entrar() {
        boolean logou = false;
        if (jTextFieldSiape.getText().equals("") || jPasswordFieldSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nenhum dos campos pode estar vazio !!!");
            jTextFieldSiape.setText("");
            jPasswordFieldSenha.setText("");
        } else {
            logou = loginControle.abreTelaPrincipalDoServidor(jTextFieldSiape.getText(), jPasswordFieldSenha.getText());
            if (!logou) {
                JOptionPane.showMessageDialog(this, "Usu√°rio e/ou senha incorreto(s) !!!");
                jTextFieldSiape.setText("");
                jPasswordFieldSenha.setText("");
            } else {
                this.dispose();
            }
        }
    }

